			<h2 style="text-align: center;">CONVOCA</h2>
			<p>A una consulta pública con la sociedad civil y organismos públicos y privados que tengan por objeto la protección y defensa de los derechos humanos para elegir o reelegir a la Presidenta o el Presidente de la Comisión de Derechos Humanos del Estado de México, al tenor del siguiente:</p>
			<h2>PROCESO</h2>
			<p>PRIMERO.- Para dar cumplimiento a lo establecido en el artículo 16 de la Constitución Política del Estado Libre y Soberano de México, la persona titular de la Comisión Estatal de Derechos Humanos para el periodo de cuatro años que inicia el 4 de agosto de 2021 será elegida o ratificada por el voto de las dos terceras partes de los miembros presentes de la Legislatura, mediante un proceso de consulta pública que será transparente, de máxima publicidad y se apegará a los términos y condiciones que determina la ley.</p>
			<p>El organismo autónomo garante se integra por cinco Comisionadas o Comisionados. Para su nombramiento, la Legislatura, previa realización de una consulta a la sociedad, a propuesta de los grupos parlamentarios, con el voto de las dos terceras partes de los miembros presentes, nombrará a la Comisionada o Comisionado que deba cubrir la vacante, siguiendo el proceso establecido en la ley.</p>
			<p>Para garantizar el principio de autonomía establecido en el artículo 30 de la Ley de Transparencia y Acceso a la Información Pública del Estado de México y Municipios, dos Comisionados o Comisionadas durarán cinco años en su cargo y tres Comisionados o Comisionadas durarán siete años en su cargo.</p>
