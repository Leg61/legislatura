		<div style="width: 100%; max-width: 688px; margin: 0px 20px; float: left; text-align: justify;">
			<h1>Convocatoria para la designación del titular del Órgano Interno de Control del Tribunal de Justicia Administrativa del Estado de México</h1>
			<p>LA H. LX LEGISLATURA, EN EJERCICIO DE LAS FACULTADES QUE LE CONFIEREN LOS ARTÍCULOS 57 Y 61 FRACCIONES I, LIV Y LVI DE LA CONSTITUCIÓN POLÍTICA DEL ESTADO LIBRE Y SOBERANO DE MÉXICO Y 38 FRACCIÓN IV DE LA LEY ORGÁNICA DEL PODER LEGISLATIVO DEL ESTADO LIBRE Y SOBERANO DE MÉXICO, HA TENIDO A BIEN EMITIR EL SIGUIENTE:</p>
			<h2 style="text-align: center">ACUERDO</h2>
			<p>POR EL QUE SE ESTABLECE EL PROCESO Y LA CONVOCATORIA PARA LA DESIGNACIÓN DEL TITULAR DEL ÓRGANO INTERNO DE CONTROL DEL TRIBUNAL DE JUSTICIA ADMINISTRATIVA DEL ESTADO DE MÉXICO.</p>
			<p><b>ARTÍCULO PRIMERO.-</b> Se aprueba el proceso para la selección y designación de la o el Titular del Órgano Interno de Control del Tribunal de Justicia Administrativa del Estado de México.</p>
			<p><b>ARTÍCULO SEGUNDO.-</b> Se aprueba la convocatoria pública abierta para el proceso de designación del Titular del Órgano Interno de Control del Tribunal de Justicia Administrativa del Estado de México, señalado en el numeral primero del presente Acuerdo, así como las etapas completas para el procedimiento, plazos y las fechas límite, los requisitos legales que deben satisfacer las y los aspirantes y los documentos que deben presentar para acreditarlos, al tenor de lo siguiente:</p>
			<p>La LX Legislatura del Estado de México<br>Con fundamento en el artículo 61 fracción LIV de la Constitución Política del Estado Libre y Soberano de México:</p>
			<h2 style="text-align: center;">CONVOCA</h2>
			<p>Al proceso de designación de ciudadanas o ciudadanos para ocupar el cargo de Titular del Órgano Interno de Control del Tribunal de Justicia Administrativa del Estado de México.</p>
			<h2>PROCESO, PLAZOS Y FECHAS LÍMITES</h2>
			<p>El proceso de designación de ciudadanas y ciudadanos para ocupar el cargo de Titular del Órgano InterntControl del Tribunal de Justicia Administrativa del Estado de México, señalado en el numeral primero del presente Acuerdo, se sujetará a lo siguiente:</p>
			<h3>APARTADO I.- DEL REGISTRO DE LOS ASPIRANTES.</h3>
			<ol>
				<li>Las y los aspirantes de esta convocatoria deberán cumplir y acreditar de manera debida, fehaciente y oportuna ante la Junta de Coordinación Política de la LX Legislatura los requisitos señalados en el artículo 81 de la Ley Orgánica del Tribunal de Justicia Administrativa del Estado de México, que a saber son:
					<ul>
						<li>Ser ciudadano mexicano en pleno goce de sus derechos civiles y políticos, y tener treinta años cumplidos el día de la designación.</li>
						<li>Gozar de buena reputación y no haber sido condenado por delito doloso que amerite pena de prisión por más de un año.</li>
						<li>Contar al momento de su designación con una experiencia de al menos cinco años en el control, manejo o fiscalización de recursos, transparencia y acceso a la información pública y de responsabilidades administrativas.</li>
						<li>Contar con título profesional relacionado con las actividades a que se refiere la fracción anterior, expedido por autoridad o institución legalmente facultada para ello;</li>
						<li>Contar con reconocida solvencia moral.</li>
						<li>No pertenecer o haber pertenecido, en los tres años anteriores a su designación, a despachos de consultoría o auditoría que hubieren prestado sus servicios al Tribunal de Justicia Administrativa, o haber fungido como consultor o auditor externo del Tribunal.</li>
						<li>No estar suspendido ni haber sido destituido o inhabilitado por resolución firme como servidor público ni estar sujeto a procedimiento de responsabilidad administrativa federal o local, en términos de las normas aplicables.</li>
						<li>No haber sido Secretario de Estado, Fiscal General de Justicia, Diputado, miembro de la Junta de Gobierno y Administración, responsable del manejo de los recursos públicos de algún partido político, ni haber sido postulado para cargo de elección popular en los tres años anteriores a la propia designación.</li>
					</ul>
				</li>
				<li>Adicionalmente presentarán:
					<ul>
						<li>Carta de solicitud de inscripción con firma autógrafa en donde se manifieste su intención de participar en el proceso de designación y de aceptar las disposiciones del mismo. </li>
						<li>Exposición de motivos de su aspiración.</li>
						<li>Currículum vitae con fotografía reciente, firmado por el o la aspirante, donde se especifique su experiencia en la materia.</li>
						<li>Carta con firma autógrafa de aceptación de las bases, procedimientos, deliberaciones y resoluciones de la presente convocatoria.</li>
					</ul>
				</li>
				<li>La documentación que acredite los requisitos será entregada el 3 de agosto, de 10:00 a 17:00 horas, en la oficina de la Secretaría Técnica de la Junta de Coordinación Política, en el recinto del Poder Legislativo, ubicado en Plaza Hidalgo s/n, Col. Centro, C.P. 50000, Toluca, México.</li>
				<li>Concluido el plazo señalado en el numeral anterior, la Junta de Coordinación Política se reunirá el 6 de agosto para realizar la revisión correspondiente a efecto de determinar aquellos aspirantes que acreditan el cumplimiento de los requisitos exigidos para ocupar el cargo.</li>
				<li>En el supuesto de que el o la aspirante no presente alguno de los documentos referidos en el numeral 1 anterior, la solicitud de registro se tendrá por no presentada.</li>
				<li>En caso de que la Junta de Coordinación Política determine que alguno de los aspirantes no cumple con alguno de los requisitos, procederá a desechar la solicitud.</li>
			</ol>
			<h3>APARTADO II.- DE LA COMPARECENCIA DE LAS Y LOS ASPIRANTES</h3>
			<ol>
				<li>La Junta de Coordinación Política analizará la información y documentación presentada por las y los aspirantes y realizará las comparecencias estipuladas en el Apartado II, número 1, inciso b), segundo párrafo, a fin de:
					<ol style="list-style-type: lower-alpha">
						<li>Verificar el cumplimiento de los requisitos contenidos en la presente convocatoria.</li>
						<li>Integrar una lista de candidatos aptos para ocupar el cargo de Titular del Órgano Interno de Control, materia del presente Acuerdo.</li>
					</ol>
				</li>
			</ol>
			<p>Las comparecencias se llevarán a cabo en reunión de la Junta de Coordinación Política, el 9 de agosto, conforme a la programación correspondiente, en las oficinas de la Presidencia de la Junta de Coordinación Política, ubicada en Plaza Hidalgo S/N, Col. Centro, Toluca, Estado de México, C.P. 50000.</p>
			<p>En su caso, la Junta de Coordinación Política determinará si las comparecencias se realizan de manera virtual y se notificará a las personas aspirantes.</p>
			<h3>APARTADO III.- DE LA SELECCIÓN DE LAS Y LOS ASPIRANTES QUE INTEGRARÁN LAS LISTAS DE CANDIDATOS Y CANDIDATAS</h3>
			<ol>
				<li>Una vez que se hayan desahogado las comparecencias, la Junta de Coordinación Política se reunirá con la finalidad de integrar y revisar los expedientes y entrevistas para la formulación del dictamen que contenga la lista de candidatos aptos para ser votados por el Pleno de la Asamblea.</li>
				<li>A más tardar el 11 de agosto, la Junta de Coordinación Política remitirá al Presidente de la LX Legislatura el dictamen, para su posterior programación en la Sesión Plenaria de Asamblea.</li>
			</ol>
			<h3>APARTADO IV.- DE LA DESIGNACIÓN DEL TITULAR DEL ÓRGANO INTERNO DE CONTROL DEL TRIBUNAL DE JUSTICIA ADMINISTRATIVA DEL ESTADO DE MÉXICO, MATERIA DEL PRESENTE ACUERDO.</h3>
			<ol>
				<li>Recibido el dictamen por la Presidencia de la Directiva de la LX Legislatura, a la mayor brevedad se incorporará en el Orden del Día de la Sesión de Legislatura para su discusión y votación.</li>
				<li>La Legislatura designará, por las dos terceras partes de los miembros presentes, a quien ocupará la titularidad del Órgano Interno de Control, materia del presente Acuerdo.</li>
			</ol>
			<p><b>ARTÍCULO TERCERO.-</b> El presente proceso será publicado en la "Gaceta del Gobierno" y en dos periódicos estatales de mayor circulación en el Estado de México.</p>
			<p><b>ARTÍCULO CUARTO.-</b> Lo no previsto en el presente Acuerdo será resuelto por la Junta de Coordinación Política.</p>
			<h2 style="text-align: center;">TRANSITORIOS</h2>
			<p><b>PRIMERO.-</b> Publíquese el presente Acuerdo en el Periódico Oficial “Gaceta del Gobierno” y en la Gaceta Parlamentaria, el miércoles 28 de julio del año en curso.</p>
			<p><b>SEGUNDO.-</b> El presente Acuerdo entrará en vigor al ser aprobado.</p>
			<p>Dado en el Palacio del Poder Legislativo, en la ciudad de Toluca de Lerdo, capital del Estado de México, a los veintisiete días del mes de julio de dos mil veintiuno.</p>
