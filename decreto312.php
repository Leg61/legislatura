		<div style="width: 100%; max-width: 688px; margin: 0px 20px; float: left; text-align: justify;">
			<h1>Convocatoria a la elección extraordinaria para renovar a las y los integrantes del Ayuntamiento de Nextlalpan, Estado de México</h1>
			<h2>DECRETO NÚMERO 312</h2>
			<p>LA H. "LX" LEGISLATURA DEL ESTADO DE MÉXICO<br>DECRETA:</p>
			<p><b>ARTÍCULO ÚNICO.-</b> En cumplimiento de la sentencia dictada en fecha 15 de julio de 2021, por el Tribunal Electoral del Estado de México, dentro del Juicio de Inconformidad JI/11/2021, se aprueba la emisión de la Convocatoria a la elección extraordinaria para renovar a las y los integrantes del Ayuntamiento de Nextlalpan, Estado de México, al tenor de lo siguiente:</p>
			<h3 style="text-align: center">LA “LX” LEGISLATURA DEL ESTADO DE MÉXICO</h3>
			<p>Con fundamento en lo dispuesto por los artículos 41 Base I y V, Apartado C, de la Constitución Política de los Estados Unidos Mexicanos; 27 numeral 2, 98 numerales 1 y 2, 104 numeral 1, incisos a), e) y f), de la Ley General de Instituciones y Procedimientos Electorales; 23 numeral 1, inciso a), de la Ley General de Partidos Políticos; 7 numeral 2, 33 numeral 1, inciso b), 75 numeral 2, 118 numeral 2, del Reglamento de Elecciones del Instituto Nacional Electoral; 61 fracción XII, 113 y 114 de la Constitución Política del Estado Libre y Soberano de México y 30, 32, 33, 35 y demás relativos y aplicables del Código Electoral del Estado de México:</p>
			<h3 style="text-align: center">CONVOCA</h3>
			<p>A la ciudadanía del Municipio de Nextlalpan, Estado de México y a los partidos políticos con registro o acreditación legal ante el Instituto Electoral del Estado de México, que tengan derecho a participar, a la elección extraordinaria para renovar a las y los integrantes del Ayuntamiento de Nextlalpan, cuya jornada electoral se celebrará el día domingo 14 de noviembre de 2021, de acuerdo con las siguientes:</p>
			<h3 style="text-align: center">BASES</h3>
			<p><b>Primera.</b> El proceso electoral extraordinario se sujetará a las disposiciones previstas en la Constitución Política de los Estados Unidos Mexicanos, la Ley General de Instituciones y Procedimientos Electorales, la Ley General de Partidos Políticos, el Reglamento de Elecciones del Instituto Nacional Electoral, la Constitución Política del Estado Libre y Soberano de México, el Código Electoral del Estado de México, así como, en los acuerdos que, en el ámbito de su competencia, emitan el Instituto Nacional Electoral y el Instituto Electoral del Estado de México.</p>
			<p><b>Segunda.</b> Los cargos de elección popular que serán renovados para el Ayuntamiento de Nextlalpan en la presente elección extraordinaria serán los siguientes:</p>
			<p>a)	Un titular de la Presidencia Municipal, propietario y su suplente.</p>
			<p>b)	Cuatro regidurías, propietarias y sus suplentes, por el principio de mayoría relativa; así como, tres regidurías de representación proporcional que correspondan.</p>
			<p>c)	Una sindicatura, propietaria y su suplente.</p>
			<p>El período constitucional del Ayuntamiento electo comprenderá del 1° de enero del año 2022 al 31 de diciembre del año 2024.</p>
			<p>En términos del artículo 35 del Código Electoral del Estado de México, en ningún caso podrá participar el partido político que tuviere suspendido o hubiere perdido su registro con anterioridad a la fecha en que estas deban realizarse. No obstante, podrá participar el partido que hubiese perdido su registro, siempre y cuando hubiera participado con candidato en la elección ordinaria que fue anulada.</p>
			<p><b>Tercera.</b> El proceso electoral extraordinario comprenderá las siguientes etapas:</p>
			<p>I.	Preparación de la elección. El Consejo General del Instituto Electoral del Estado de México declarará el inicio de los trabajos relacionados a la preparación de la elección extraordinaria para renovar a las y los integrantes del Ayuntamiento de Nextlalpan, ajustando los plazos relativos a las distintas etapas del proceso electoral establecidos en el citado ordenamiento, de conformidad con lo dispuesto por el artículo 30 del Código Electoral del Estado de México.</p>
			<p>Los plazos y términos señalados, deberán ser publicados oportunamente en el Periódico Oficial "Gaceta del Gobierno" y en dos periódicos locales y uno nacional de mayor circulación.</p>
			<p>II.	Jornada Electoral. Iniciará a las 8:00 horas del día domingo 14 de noviembre de 2021 y concluirá con la clausura de las casillas y remisión de los paquetes electorales al Consejo Municipal Electoral de Nextlalpan.</p>
			<p>III.	Resultados y declaración de validez. Iniciará con la recepción de los paquetes electorales al Consejo Municipal Electoral de Nextlalpan, para continuar con el cómputo de la elección, y concluirá con la entrega de constancias de mayoría y la declaratoria de validez de la elección para renovar a las y los integrantes del Ayuntamiento de Nextlalpan hecha por Consejo Municipal Electoral o, en su caso, con las resoluciones que emitan los tribunales electorales competentes.</p>
			<p><b>Cuarta.</b> El proceso electoral extraordinario se sujetará al Calendario Electoral que para tal efecto apruebe el Consejo General del Instituto Electoral del Estado de México.</p>
			<p><b>Quinta.</b> La ciudadanía en general, de conformidad con los requisitos establecidos en la normatividad electoral vigente, podrá participar como observadoras y observadores electorales, así como, en el ejercicio de su derecho al sufragio en el proceso electoral extraordinario.</p>
			<p><b>Sexta.</b> En la elección extraordinaria podrán votar las personas que se encuentren inscritas en la Lista Nominal de Electores de Nextlalpan, utilizada en la Jornada Electoral del 6 de junio de 2021.</p>
			<p><b>Séptima.</b> En términos de los artículos 5, 86, de la Constitución Política del Estado Libre y Soberano de México y 5 del Código Electoral del Estado de México, todas las autoridades del Estado y de los Municipios, en el ámbito de sus respectivas competencias, están obligadas a auxiliar a las autoridades electorales para el correcto cumplimiento de sus obligaciones.</p>
			<p><b>Octava.</b> Los casos no previstos en esta Convocatoria y los que se presenten durante el proceso electoral extraordinario serán resueltos por el Consejo Municipal Electoral de Nextlalpan, el Consejo General del Instituto Electoral del Estado de México o, en su caso, el Instituto Nacional Electoral, atendiendo a lo dispuesto en los ordenamientos jurídicos aplicables.</p>
			<h3 style="text-align: center">T R A N S I T O R I O S</h3>
			<p><b>ARTÍCULO PRIMERO.-</b> Publíquese el presente Decreto en el Periódico Oficial "Gaceta del Gobierno" y en dos periódicos locales y en uno nacional de mayor circulación en el Estado.</p>
			<p><b>ARTÍCULO SEGUNDO.-</b> El presente Decreto entrará en vigor el día de su publicación en el Periódico Oficial "Gaceta del Gobierno".</p>
			<p>Lo tendrá entendido el Gobernador del Estado, haciendo que se publique y se cumpla.</p>
			<p>Dado en el Palacio del Poder Legislativo, en la ciudad de Toluca de Lerdo, capital del Estado de México, a los trece días del mes de agosto del año dos mil veintiuno.</p>
